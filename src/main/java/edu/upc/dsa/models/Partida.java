package edu.upc.dsa.models;

public class Partida {
    String Dificultad;
    int Duración;

    public String getDificultad() {
        return Dificultad;
    }

    public void setDificultad(String dificultad) {
        Dificultad = dificultad;
    }

    public int getDuración() {
        return Duración;
    }

    public void setDuración(int duración) {
        Duración = duración;
    }
}
